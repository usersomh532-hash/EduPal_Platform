def get_subjects_for_specialization(specialization: str) -> list:
    """
    تُعيد قائمة المواد المتاحة لتخصص معين.

    المنطق:
      1. إذا كان التخصص موجوداً في SUBJECT_MAP → أعد مواده
      2. إذا لم يكن موجوداً (تخصص قديم أو غير معروف) → أعد قائمة فارغة
         (الـ view سيتعامل مع الحالة هذه بإظهار كل المواد)
    """
    spec = (specialization or '').strip()
    if not spec or spec == 'General':
        return []
    return SUBJECT_MAP.get(spec, [])


def get_all_subjects_flat() -> list:
    """
    تُعيد قائمة بجميع المواد من كل التخصصات (بدون تكرار، مرتبة).
    تُستخدم كـ fallback للتخصصات غير المعروفة أو Admin.
    """
    seen, result = set(), []
    for subjects in SUBJECT_MAP.values():
        for s in subjects:
            if s not in seen:
                seen.add(s)
                result.append(s)
    return sorted(result)