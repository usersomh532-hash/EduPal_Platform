teachers = Teacher.objects.select_related('userid').all()
fixed    = []
ok       = []
need_manual = []

for t in teachers:
    username = t.userid.username if t.userid else '—'
    fullname = t.userid.fullname if t.userid else '—'
    spec     = (t.specialization or '').strip()
    direc    = (t.directorate   or '').strip()
    issues   = []
    changes  = {}

    # ── فحص التخصص ──────────────────────────────────────────
    if not spec or spec == 'General':
        issues.append(f'التخصص فارغ أو General: "{spec}"')
    elif spec not in VALID_SPEC_VALUES:
        # حاول إصلاح تلقائي
        mapped = VALID_SPECIALIZATIONS.get(spec.strip().lower())
        if not mapped:
            mapped = VALID_SPECIALIZATIONS.get(spec.strip())
        if mapped and mapped in VALID_SPEC_VALUES:
            changes['specialization'] = mapped
            issues.append(f'التخصص "{spec}" → سيُصلح إلى "{mapped}"')
        else:
            issues.append(f'⚠️  التخصص "{spec}" غير معروف — يحتاج تدخل يدوي')

    # ── فحص المديرية ────────────────────────────────────────
    if not direc:
        issues.append('المديرية فارغة')
    elif direc not in VALID_DIRECTORATES:
        issues.append(f'⚠️  المديرية "{direc}" غير معروفة — تحقق منها')

    # ── تطبيق التغييرات التلقائية ────────────────────────────
    if changes:
        for field, val in changes.items():
            setattr(t, field, val)
        t.save(update_fields=list(changes.keys()))
        fixed.append((username, fullname, issues))
    elif issues:
        need_manual.append((username, fullname, issues, spec, direc))
    else:
        ok.append((username, fullname, spec, direc))

# ── تقرير النتائج ────────────────────────────────────────────
